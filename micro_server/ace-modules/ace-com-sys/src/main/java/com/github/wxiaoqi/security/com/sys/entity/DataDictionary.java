package com.github.wxiaoqi.security.com.sys.entity;

import javax.persistence.*;

import lombok.Data;

/**
 * 数据字典
 */
@Table(name = "com_data_dictionary")
@Data
public class DataDictionary {

    /**
     * 类型
     */
    private String type;

    /**
     * 名称
     */
    @Id
    private String name;

    /**
     * 代码
     */
    private String code;


    /**
     * 备注
     */
    private String remark;
}
