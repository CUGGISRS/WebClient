package com.github.wxiaoqi.security.info.sys.entity;

import javax.persistence.*;

@Table(name = "zsxx_company_info")
public class CompanyInfo {
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Integer id;

    /**
     * 企业名称
     */
    @Column(name = "company_name")
    private String companyName;

    /**
     * 企业类型
     */
    @Column(name = "company_type")
    private String companyType;

    /**
     * 负责人
     */
    @Column(name = "charge_man")
    private String chargeMan;

    /**
     * 联系电话
     */
    private String phone;

    /**
     * 注册资金
     */
    @Column(name = "regist_money")
    private String registMoney;

    /**
     * 图片
     */
    @Column(name = "company_img")
    private Integer companyImg;

    /**
     * 营业执照代码
     */
    @Column(name = "business_code")
    private String businessCode;

    /**
     * 行业
     */
    private String industry;

    /**
     * 介绍
     */
    private String introduction;

    /**
     * 企业所在地
     */
    private String place;

    /**
     * 是否删除
     */
    @Column(name = "is_deleted")
    private Integer isDeleted;

    /**
     * @return id
     */
    public Integer getId() {
        return id;
    }

    /**
     * @param id
     */
    public void setId(Integer id) {
        this.id = id;
    }

    /**
     * 获取企业名称
     *
     * @return company_name - 企业名称
     */
    public String getCompanyName() {
        return companyName;
    }

    /**
     * 设置企业名称
     *
     * @param companyName 企业名称
     */
    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }

    /**
     * 获取企业类型
     *
     * @return company_type - 企业类型
     */
    public String getCompanyType() {
        return companyType;
    }

    /**
     * 设置企业类型
     *
     * @param companyType 企业类型
     */
    public void setCompanyType(String companyType) {
        this.companyType = companyType;
    }

    /**
     * 获取负责人
     *
     * @return charge_man - 负责人
     */
    public String getChargeMan() {
        return chargeMan;
    }

    /**
     * 设置负责人
     *
     * @param chargeMan 负责人
     */
    public void setChargeMan(String chargeMan) {
        this.chargeMan = chargeMan;
    }

    /**
     * 获取联系电话
     *
     * @return phone - 联系电话
     */
    public String getPhone() {
        return phone;
    }

    /**
     * 设置联系电话
     *
     * @param phone 联系电话
     */
    public void setPhone(String phone) {
        this.phone = phone;
    }

    /**
     * 获取注册资金
     *
     * @return regist_money - 注册资金
     */
    public String getRegistMoney() {
        return registMoney;
    }

    /**
     * 设置注册资金
     *
     * @param registMoney 注册资金
     */
    public void setRegistMoney(String registMoney) {
        this.registMoney = registMoney;
    }

    /**
     * 获取图片
     *
     * @return company_img - 图片
     */
    public Integer getCompanyImg() {
        return companyImg;
    }

    /**
     * 设置图片
     *
     * @param companyImg 图片
     */
    public void setCompanyImg(Integer companyImg) {
        this.companyImg = companyImg;
    }

    /**
     * 获取营业执照代码
     *
     * @return business_code - 营业执照代码
     */
    public String getBusinessCode() {
        return businessCode;
    }

    /**
     * 设置营业执照代码
     *
     * @param businessCode 营业执照代码
     */
    public void setBusinessCode(String businessCode) {
        this.businessCode = businessCode;
    }

    /**
     * 获取行业
     *
     * @return industry - 行业
     */
    public String getIndustry() {
        return industry;
    }

    /**
     * 设置行业
     *
     * @param industry 行业
     */
    public void setIndustry(String industry) {
        this.industry = industry;
    }

    /**
     * 获取介绍
     *
     * @return introduction - 介绍
     */
    public String getIntroduction() {
        return introduction;
    }

    /**
     * 设置介绍
     *
     * @param introduction 介绍
     */
    public void setIntroduction(String introduction) {
        this.introduction = introduction;
    }

    /**
     * 获取企业所在地
     *
     * @return place - 企业所在地
     */
    public String getPlace() {
        return place;
    }

    /**
     * 设置企业所在地
     *
     * @param place 企业所在地
     */
    public void setPlace(String place) {
        this.place = place;
    }

    /**
     * 获取是否删除
     *
     * @return is_deleted - 是否删除
     */
    public Integer getIsDeleted() {
        return isDeleted;
    }

    /**
     * 设置是否删除
     *
     * @param isDeleted 是否删除
     */
    public void setIsDeleted(Integer isDeleted) {
        this.isDeleted = isDeleted;
    }
}
