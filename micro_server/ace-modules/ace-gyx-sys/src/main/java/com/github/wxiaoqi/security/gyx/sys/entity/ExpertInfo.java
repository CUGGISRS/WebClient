package com.github.wxiaoqi.security.gyx.sys.entity;

import javax.persistence.*;

@Table(name = "gyx_expert_info")
public class ExpertInfo {
    @Id
    private Integer id;

    /**
     * 专家名称
     */
    @Column(name = "expert_name")
    private String expertName;

    /**
     * 联系电话
     */
    private String phone;

    /**
     * 微信号
     */
    private String wechat;

    /**
     * 业务专长
     */
    private String expertise;

    /**
     * 职称
     */
    @Column(name = "job_title")
    private String jobTitle;

    /**
     * 服务地区
     */
    @Column(name = "service_area")
    private String serviceArea;

    /**
     * 照片
     */
    private String photo;

    /**
     * 专家简介
     */
    private String introduction;

    /**
     * 是否删除
     */
    @Column(name = "is_deleted")
    private Integer isDeleted;

    /**
     * @return id
     */
    public Integer getId() {
        return id;
    }

    /**
     * @param id
     */
    public void setId(Integer id) {
        this.id = id;
    }

    /**
     * 获取专家名称
     *
     * @return expert_name - 专家名称
     */
    public String getExpertName() {
        return expertName;
    }

    /**
     * 设置专家名称
     *
     * @param expertName 专家名称
     */
    public void setExpertName(String expertName) {
        this.expertName = expertName;
    }

    /**
     * 获取联系电话
     *
     * @return phone - 联系电话
     */
    public String getPhone() {
        return phone;
    }

    /**
     * 设置联系电话
     *
     * @param phone 联系电话
     */
    public void setPhone(String phone) {
        this.phone = phone;
    }

    /**
     * 获取微信号
     *
     * @return wechat - 微信号
     */
    public String getWechat() {
        return wechat;
    }

    /**
     * 设置微信号
     *
     * @param wechat 微信号
     */
    public void setWechat(String wechat) {
        this.wechat = wechat;
    }

    /**
     * 获取业务专长
     *
     * @return expertise - 业务专长
     */
    public String getExpertise() {
        return expertise;
    }

    /**
     * 设置业务专长
     *
     * @param expertise 业务专长
     */
    public void setExpertise(String expertise) {
        this.expertise = expertise;
    }

    /**
     * 获取职称
     *
     * @return job_title - 职称
     */
    public String getJobTitle() {
        return jobTitle;
    }

    /**
     * 设置职称
     *
     * @param jobTitle 职称
     */
    public void setJobTitle(String jobTitle) {
        this.jobTitle = jobTitle;
    }

    /**
     * 获取服务地区
     *
     * @return service_area - 服务地区
     */
    public String getServiceArea() {
        return serviceArea;
    }

    /**
     * 设置服务地区
     *
     * @param serviceArea 服务地区
     */
    public void setServiceArea(String serviceArea) {
        this.serviceArea = serviceArea;
    }

    /**
     * 获取照片
     *
     * @return photo - 照片
     */
    public String getPhoto() {
        return photo;
    }

    /**
     * 设置照片
     *
     * @param photo 照片
     */
    public void setPhoto(String photo) {
        this.photo = photo;
    }

    /**
     * 获取专家简介
     *
     * @return introduction - 专家简介
     */
    public String getIntroduction() {
        return introduction;
    }

    /**
     * 设置专家简介
     *
     * @param introduction 专家简介
     */
    public void setIntroduction(String introduction) {
        this.introduction = introduction;
    }

    /**
     * 获取是否删除
     *
     * @return is_deleted - 是否删除
     */
    public Integer getIsDeleted() {
        return isDeleted;
    }

    /**
     * 设置是否删除
     *
     * @param isDeleted 是否删除
     */
    public void setIsDeleted(Integer isDeleted) {
        this.isDeleted = isDeleted;
    }
}